package Bucles;

public class For {
    public static void main(String[] args) {
        // el for te tres parts separades per ;
        // la primera part es per inicialitzacio, per exemple inicialitzam la i a 0
        // la segona part significa que la se reoetira fins que se comlesqui la condicio
        // la tercera part es per modificar la condicio perque la condicio deixi de ser verdadera
        // i++ es lo mateix a i = i + 1
        for (int i = 0; i < 8; i++) {
            System.out.println(i);
        }
    }
}
